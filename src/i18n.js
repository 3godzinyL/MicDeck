export function translate(language, key, variables = {}) {
  let value = copy[language]?.[key] ?? copy.en[key] ?? key;
  Object.entries(variables).forEach(([name, replacement]) => {
    value = value.replaceAll(`{${name}}`, String(replacement));
  });
  return value;
}
